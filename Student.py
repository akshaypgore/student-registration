class Student:

    def __init__(self, firstname, lastname, fullname, age, subjects, course_choice, gender_of_student, student_bio):
        self.firstname = firstname
        self.lastname  = lastname
        self.fullname  = fullname
        self.age       = age
        self.subjects  = subjects
        self.course_choice = course_choice
        self.gender_of_student = gender_of_student
        self.student_bio = student_bio
        